grade = int(input("학점을 기입하십시오 :"))

if grade >= 95 :        
            print("A+")
elif grade >= 90 :
            print("A")
elif grade >= 85 :
            print("B+")
elif grade >= 80 :
            print("B")
elif grade >= 75 :
            print("c+")
elif grade >= 70 :
            print("c")
elif grade >= 65 :
            print("d+")
elif grade >= 60 :
            print("d")
else :
            print("F")
